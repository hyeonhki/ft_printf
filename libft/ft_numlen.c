int	ft_numlen(int n)
{
	int cnt;

	cnt = 0;
	if (n < 0)
	{
		cnt += 1;
		if (n == -2147483648)
			return (11);
		else
			n *= -1;
	}
	if (n >= 0 && n <= 9)
	{
		cnt += 1;
		return (cnt);
	}
	while (n != 0)
	{
		n = n / 10;
		cnt += 1;
	}
	return (cnt);
}
